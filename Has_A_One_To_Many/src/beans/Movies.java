package beans;

public class Movies {
	
	private int mid; 
	private String moviesname;
	private int aid;
	
	
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	
	public String getMoviesname() {
		return moviesname;
	}
	public void setMoviesname(String moviesname) {
		this.moviesname = moviesname;
	}
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
}